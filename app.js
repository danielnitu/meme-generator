var main = function() {
  var $topText;
	$('#top-text').keyup(function() {
    $topText = $(this).val();
    $('.top-caption').text($topText);
  });
  
  var $bottomText;
  $('#bottom-text').keyup(function() {
    $bottomText = $(this).val();
    $('.bottom-caption').text($bottomText);
  });
  
  var $imageUrl;
  $('#image-url').keyup(function() {
    $imageUrl = $(this).val();
    if (validUrl($imageUrl)) {
      $('.meme img').attr('src', $imageUrl);
    } else {
      $(this).val('Enter a valid URL');
    }
  });
  
  $('#image-url, #top-text, #bottom-text').on('focus', function() {
    $(this).val('');
  }).on('blur', function() {
    if ($(this).is('#top-text')) {
      $(this).val($topText);
    } else if ($(this).is('#bottom-text')) {
      $(this).val($bottomText);
    } else if ($(this).is('#image-url')) {
      $(this).val($imageUrl);
    }
  });
};

$(document).ready(main);